#' Function to subtract two numbers
#'
#' @param num1 numeric value
#' @param num2 numeric value
#'
#' @return numeric value which is the subtraction of the second number from the first number
#' @export
#'
#' @examples subtract_two_numbers(7, 11)
subtract_two_numbers <- function(num1, num2) {
  total <- num1 - num2
  total
}
